//this program is only for find sin,cos,tan
#include<stdio.h>
int main()
{
    int x;
    printf("Enter value of x:");
    scanf("%d",&x);
    printf("Exponential of x is:%d\n",exp(x));
    printf("sin of x is:%d\n",sin(x));
    printf("cos of x is:%d\n",cos(x));
    printf("tan of is:%d\n",tan(x));
}
